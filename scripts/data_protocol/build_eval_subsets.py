#!/usr/bin/env python3

"""Materialise locked full-scale evaluation subset manifests."""

from __future__ import annotations

import argparse
import sys
from pathlib import Path

import pandas as pd

PROJECT_ROOT = Path(__file__).resolve().parents[2]
if str(PROJECT_ROOT) not in sys.path:
    sys.path.insert(0, str(PROJECT_ROOT))

from src.data.subset_definitions import (
    ANONYMISATION_EVAL_SUBSET,
    DETECTION_EVAL_SUBSET,
    no_overlap_check,
    resolve_subset,
)


DEFAULT_MANIFEST = PROJECT_ROOT / "data" / "castle2024" / "raw_dataset_index.csv"
DEV_SET_PATH = PROJECT_ROOT / "outputs" / "01_protocol" / "supporting_protocols" / "01_development_300.csv"
CALIBRATION_SET_PATH = PROJECT_ROOT / "outputs" / "01_protocol" / "supporting_protocols" / "02_calibration_200.csv"
FID_SET_PATH = PROJECT_ROOT / "outputs" / "01_protocol" / "supporting_protocols" / "03_fid_source_50000.csv"
DETECTION_OUTPUT = PROJECT_ROOT / "outputs" / "01_protocol" / "thesis_manifests" / "final_face_detection_500.csv"
ANONYMISATION_OUTPUT = PROJECT_ROOT / "outputs" / "01_protocol" / "thesis_manifests" / "final_face_anonymisation_500.csv"


def parse_args() -> argparse.Namespace:
    parser = argparse.ArgumentParser(description=__doc__)
    parser.add_argument("--manifest", default=str(DEFAULT_MANIFEST.relative_to(PROJECT_ROOT)))
    parser.add_argument("--detection-output", default=str(DETECTION_OUTPUT.relative_to(PROJECT_ROOT)))
    parser.add_argument("--anonymisation-output", default=str(ANONYMISATION_OUTPUT.relative_to(PROJECT_ROOT)))
    return parser.parse_args()


def _blocked_paths() -> set[str]:
    blocked: set[str] = set()
    for path in (DEV_SET_PATH, CALIBRATION_SET_PATH, FID_SET_PATH):
        if path.exists():
            df = pd.read_csv(path)
            blocked.update(df["relative_path"].astype(str).tolist())
    return blocked


def _normalise_subset(df: pd.DataFrame, seed: int) -> pd.DataFrame:
    result = df.copy().reset_index(drop=True)
    result["selection_seed"] = int(seed)
    return result


def build_eval_subsets(manifest_path: Path) -> dict[str, pd.DataFrame]:
    """Build the locked face-detection and anonymisation subsets.

    The multimodal protocol is reviewer-curated and must not be regenerated by
    generic random sampling.
    """
    manifest_df = pd.read_csv(manifest_path)
    filtered_df = manifest_df[~manifest_df["relative_path"].astype(str).isin(_blocked_paths())].reset_index(drop=True)

    detection_df = _normalise_subset(resolve_subset(DETECTION_EVAL_SUBSET, filtered_df), DETECTION_EVAL_SUBSET["seed"])
    used_detection = set(detection_df["relative_path"].astype(str).tolist())

    anonymisation_pool = filtered_df[~filtered_df["relative_path"].astype(str).isin(used_detection)].reset_index(drop=True)
    anonymisation_df = _normalise_subset(
        resolve_subset(ANONYMISATION_EVAL_SUBSET, anonymisation_pool),
        ANONYMISATION_EVAL_SUBSET["seed"],
    )
    subset_frames: dict[str, pd.DataFrame] = {
        "DETECTION_EVAL_SUBSET": detection_df,
        "ANONYMISATION_EVAL_SUBSET": anonymisation_df,
    }
    if DEV_SET_PATH.exists():
        subset_frames["DEV_SET"] = pd.read_csv(DEV_SET_PATH)
    if CALIBRATION_SET_PATH.exists():
        subset_frames["CALIBRATION_SET"] = pd.read_csv(CALIBRATION_SET_PATH)
    if FID_SET_PATH.exists():
        subset_frames["FID_SUBSET"] = pd.read_csv(FID_SET_PATH)
    no_overlap_check(subset_frames)
    return subset_frames


def main() -> None:
    args = parse_args()
    subsets = build_eval_subsets(PROJECT_ROOT / args.manifest)
    outputs = {
        "DETECTION_EVAL_SUBSET": PROJECT_ROOT / args.detection_output,
        "ANONYMISATION_EVAL_SUBSET": PROJECT_ROOT / args.anonymisation_output,
    }
    for name, output_path in outputs.items():
        output_path.parent.mkdir(parents=True, exist_ok=True)
        subsets[name].to_csv(output_path, index=False)
        print(f"Saved {name} to {output_path.relative_to(PROJECT_ROOT)}")


if __name__ == "__main__":
    main()
